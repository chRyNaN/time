package com.chrynan.time

/**
 * An enum class representing the different days of a week. This enum consists of the days Sunday, Monday, Tuesday,
 * Wednesday, Thursday, Friday, and Saturday. This enum class has no reference to a particular time or [Moment],
 * instead it is just an indicator of the named day.
 *
 * @author chRyNaN
 */
enum class DayOfWeek {

    SUNDAY,
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY
}